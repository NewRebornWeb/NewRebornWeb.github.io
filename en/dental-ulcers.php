					<h1>Buccal ulcers: what are they, causes, treatments</h1>
					<h3>Posted by Luis Fernando Montaño Chavez | <span>Nov 17, 2021</span></h3>
					<p>A canker sore (from the ancient Greek ἄφθαι, aphtai, "burns") is a small, rounded, whitish, superficial ulcer with a well-defined red border; of unknown origin, appearing during the course of certain diseases. It is usually recurring. It forms on the mucosa of the mouth or other parts of the digestive tract, or on the genital mucosa; As the most common presentation is the oropharyngeal, it is frequently used in a more restricted sense, referring only to canker sore.</p>
					<div class="linea-img">
						<img src="https://www.colegiodentistas.cl/inicio/wp-content/uploads/2017/05/odontologia-e1519648397522.jpg">
					</div>
				</div>
				<div class="info-post-blog">
					<hr>
					<h2 id="subtitulo1">WHAT ARE THEY</h2>
					<p>Buccal or oral thrush or aphthous stomatitis or mouth ulcer is a mucosal lesion or erosion, such as a small wound or sore, which is generally located on the oral mucosa with flat and regular edges and surrounded by an area of erythema.</p>
					<br>
					<p>Canker sores are one of the most frequent lesions of the oral cavity with a prevalence between 5 and 80% of the population. It occurs with great frequency among children and adolescents, especially between 10 and 19 years of age.</p>
					<br>
					<br>
					<hr>
					<h2 id="subtitulo2">CAUSES</h2>
					<p>The exact cause of canker sores is still unknown and varies from person to person. Still, there are certain common causes and various factors that can aggravate canker sores, including the following:
						<br>
						<br>
					<p>
						-Give up smoking
						<br>
						-Eat citrus fruits and other foods with high acidity or spices
						<br>
						-biting the tongue or the inside of the cheek
						<br>
						-Use of braces, ill-fitting dentures, and other appliances that may rub against the mouth
						<br>
						-A poor dental filling
						<br>
						-Stress or anxiety
						<br>
						-Hormonal changes during pregnancy, puberty and menopause
						<br>
						-Medications including beta-blockers and pain relievers
						<br>
						-Genetic factors
					</p>
					<br>
					<br>
					<img src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcRmy5Bmfo1yuUxdvFT2RWSicEfFXg1Tu4_lzw&usqp=CAU">
					<br><br>
					<hr>
					<h2 id="subtitulo3">TREATMENTS</h2><br>
					<p>The first step, before starting treatment, is to identify the possible causes that cause the appearance of canker sores, such as nutritional deficiencies or allergies.
						<br><br>
						General practitioners can evaluate and diagnose certain environmental factors, but the possible presence of a systemic disease causing thrush, such as celiac disease, Behçet's disease or AIDS, must be studied by the appropriate specialists, since their recognition , diagnosis and treatment is very difficult for GPs.
						<br><br>
						In the case of celiac disease, treatment with a gluten-free diet resolves canker sores in approximately 90% of patients. The persistence of lesions in a minority of celiac patients is considered an indication of possible lack of strict adherence to the diet, since exposure to minimal amounts of gluten can cause the development or reappearance of lesions in the mouth.
						<br>
					</p>
