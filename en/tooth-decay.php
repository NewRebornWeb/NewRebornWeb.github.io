					<h2 id="subtitulo1">WHAT ARE THEY</h2>
					<p>Cavities are permanently damaged areas on the surface of the teeth that develop into small openings or holes. Cavities occur due to a combination of factors, including bacteria in the mouth, frequent snacking, sugary drinks, and poor dental cleaning.</p>
					<br>
					<p>Dental cavities are one of the most common health problems in the world. They appear especially frequently in children, adolescents and older adults. However, everyone who has teeth can get cavities, even babies.</p>
					<br>
					<p>If left untreated, cavities can spread and affect deeper layers of the teeth. They can cause severe toothache, infections, and tooth loss. The best protection measures against tooth decay are regular dental visits and good brushing and flossing habits.</p>
					<br>
					<hr>
					<h2 id="subtitulo2">CAUSES</h2>
					<p>Dental cavities are a process that occurs over time. This is how dental cavities form:
						<br>
						<br>
						<span><b>° Plaque formation.</b></span> Dental plaque is a transparent sticky film that covers the teeth. It is generated by eating a lot of sugars and starches and not brushing your teeth well. When sugars and starches are not cleaned from your teeth, bacteria quickly begin to feed on them and form plaque. Plaque that remains on the teeth can harden below or above the gum line and turn into tartar. Tartar makes it harder to remove plaque and creates a shield for bacteria.
						<br>
						<br>
						<span><b>° Plaque attacks.</b></span> The acids in plaque remove minerals from the hard, outer enamel of teeth. This erosion causes tiny openings or holes in the enamel, the first stage of cavities. Once parts of the enamel are worn away, bacteria and acid reach the next layer of the teeth, called "dentin." This layer is softer than enamel and less resistant to acid. The dentin has small tubes that communicate directly with the nerve of the tooth and cause sensitivity.
						<br>
						<br>
						<span><b>° The destruction continues.</b></span> As dental cavities form, bacteria and acid continue their march through the teeth, then moving to the material inside the tooth ( pulp) containing nerves and blood vessels. The pulp becomes inflamed and irritated by bacteria. Since there is no room for inflammation to expand inside a tooth, the nerve is squeezed, causing pain. The discomfort can even extend outside the root of the tooth, towards the bone.
					</p>
					<br>
					<br>
					<img src="https://images.pexels.com/photos/298611/pexels-photo-298611.jpeg?auto=compress&cs=tinysrgb&dpr=2&h=650&w=940">
					<br>
					<hr>
					<h2 id="subtitulo3">TREATMENT</h2>
					<p>Regular checkups can identify cavities and other dental diseases before they cause troublesome symptoms and lead to more serious problems. The sooner you seek medical attention, the more likely you are to reverse the early stages of tooth decay and prevent it from progressing. If a cavity is treated before it starts to cause pain, you probably won't need long-term treatment.
						<br><br>
						Caries treatment depends on the severity and your particular situation. Some treatment options are as follows:
						<br><br>
						<span><b>° Fluoride treatments.</b></span> If a cavity is just starting to appear, fluoride treatment can help restore tooth enamel and sometimes reverse the decay in the tooth. very early stages. Professional fluoride treatments contain more fluoride than the amount found in tap water, toothpaste, and mouthwash. Fluoride treatments can be liquid, gel, foam, or varnish that is brushed onto the teeth or placed in a small tray that fits over the teeth.
						<br><br>
						<span><b>° Fillings.</b></span> Fillings, sometimes called “restorations”, are the main treatment option when decay has progressed beyond the first stage. Fillings are made from various materials, such as tooth-colored composite resins, porcelain, or dental amalgam, which is a combination of different materials.
						<br><br>
						<span><b>° Crowns.</b></span> If you have extensive cavities or weakened teeth, you may need a crown – a custom-made covering that replaces the entire natural crown of the tooth. The dentist will drill the decayed area and other parts of the tooth to ensure a good fit. Crowns can be made of gold, high-strength porcelain, resin, porcelain-fused-to-metal, or other materials.
						<br><br>
						<span><b>° Root Canals.</b></span> When decay reaches the inside of the tooth (pulp), you may need root canals. This is a treatment to repair and preserve a badly damaged or infected tooth instead of extracting it. The pulp of the diseased tooth is extracted. Medicines are sometimes put into the root canal to clean out the infection. The pulp is then replaced with a filling.
						<br><br>
						<span><b>° Tooth extraction.</b></span> Some teeth have such severe decay that they cannot be restored and must be extracted. Tooth extraction can leave a gap that causes other teeth to shift. If possible, consider getting a bridge or dental implant to replace the missing tooth.
					</p>
