					<h1>Tooth decay: what are they, causes and treatments</h1>
					<h3>Posted by Luis Fernando Montaño Chavez | <span>Nov 17, 2021</span></h3>
					<p>Cavities, our archenemies of all life. From children to adults.</p>
					<div class="linea-img">
						<img src="https://bqdentalcenters.es/wp-content/uploads/tipos-de-caries-palas-centrales.jpg">
