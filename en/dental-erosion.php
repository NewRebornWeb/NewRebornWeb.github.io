					<h1>Dental erosion</h1>
					<h3>Posted by Luis Fernando Montaño Chavez | <span>Nov 20, 2021</span></h3>
					<p>Dental erosion, more than harming our oral health, also affects our smile for various reasons.</p>
					<div class="linea-img">
						<img src="https://blog.uchceu.es/odontologia/wp-content/uploads/sites/7/2014/04/erosion-dental-dieta-e1398415900737.jpg">
					</div>
				</div>
				<div class="info-post-blog">
					<hr>
					<h2 id="subtitulo1">WHAT ARE THEY AND THEIR CAUSES</h2>
					<p>Dental erosion is one of the different types of dental wear that exist, along with abrasion,
						attrition and abfraction.</p>
					<br>
					<p>In the case of dental erosion, this happens when there is damage to the tooth enamel caused by
						acids other than those present in bacterial plaque. Thus, two types of
						erosion:</p>
					<br>
					<span><b>° Intrinsic erosion.</b></span> When the cause of the erosion is produced by the organism itself
					human, such as gastric acids. It occurs frequently in people who suffer from stomach or throat problems.
					feeding.
					<span><b>° Extrinsic erosion.</b></span> On the other hand, there is another type of dental erosion caused by
					external agents, such as the intake of certain medications (chewable vitamin C supplements or
					those containing hydrochloric acid) or due to certain <b>harmful eating habits</b> for
					dental health: such as a diet excessive in sugars, carbonated drinks, concentrated fruit juices and
					other foods that help create a highly acidic environment in the oral cavity.
					<br>
					<br>
					<br>
					<hr>
					<h2 id="subtitulo2">SYMPTOMS</h2>
					<p>Dental enamel is the part that covers the outside of the teeth, whose function is to protect the
						internal structure of the tooth from external pathogens, therefore, the weakening of the enamel to
						The cause of dental erosion involves the exposure of the dentin (the yellowish part of the tooth).
					</p>
					<br>
					<p>Some of the symptoms of dental erosion are the following:</p>
					<br>
					<span><b>° Tooth discoloration:</b></span> if the enamel is being damaged due to dental erosion, the
					The natural white of the teeth is gradually lost, giving way to a more yellowish tone.
					<span><b>° Tooth sensitivity:</b></span> When the dentin is exposed with dental erosion, the teeth
					they are much more sensitive to external agents. For this reason, it is common to feel a sharp pain after
					the intake of certain foods, sugary foods or drinks, especially hot or cold.
					<span><b>° Wear of the teeth:</b></span> the teeth can change shape, look more
					rounded or appear small fissures. Some transparencies may also appear on the edges
					teeth, losing these their opacity.
					<br>
					<img src="https://q5g4e3i5.rocketcdn.me/app/uploads/erosion-dientes-640x300.jpg">
					<br>
					<hr>
					<h2 id="subtitulo3">TREATMENTS</h2>
					<p>Once the first symptoms of dental erosion are identified, it is advisable to go to a
						dentist to make the diagnosis, and depending on the case, prescribe the most appropriate treatment
						for each case.</p>
					<br>
					<p>To prescribe the appropriate treatment for dental erosion, the specialist must first investigate
						place the causes that cause enamel damage. And once determined, we find three types of
						Different treatments depending on the degree of advancement of dental erosion.</p>
					<br>
					<p>For cases of initial erosion, the patient is usually recommended a series of preventive guidelines that
						You must comply rigorously if you do not want the condition to go further. With these guidelines, it would be avoided
						the harmful consequences that would affect the enamel and the dental health of the patient.</p>
					<br>
					<p>In the event that dental erosion is moderate, and the enamel has lost thickness locally in
						the tooth, leaving the dentin exposed, then a composite filling will have to be made.</p>
					<br>
					<p>If severe erosion and loss of enamel has affected several areas of the same tooth or several
						teeth, and the patient suffers from acute dental sensitivity, then reconstruction of
						affected teeth. In the most serious cases, you can even resort to performing a
						endodontics or the placement of inlays or dental crowns.</p>
