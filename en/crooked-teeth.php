					<h1>Crooked teeth: effects, causes y treatments</h1>
					<h3>Posted by Luis Fernando Montaño Chavez | <span>Nov 17, 2021</span></h3>
					<p>Crooked teeth have been the main reason for ridicule, insecurities, harassment and mistreatment for society in general, but nevertheless, beyond giving an aesthetic treatment, it is for one's own health, since having crooked teeth can affect future.</p>
					<div class="linea-img">
						<img src="https://morapavic.cl/wp-content/uploads/2018/08/dientes-chuecos-factor-hereditario-mora-pavic-1.jpg">
					</div>
				</div>
				<div class="info-post-blog">
					<hr>
					<h2 id="subtitulo1">EFFECTS</h2>
					<p>Dental crowding is the medical name for crooked teeth. It occurs when there is a lack of space and/or the
						teeth are large. As the teeth erupt they fit into the bone any way they can, resulting in rotated and
						misaligned teeth. In severe cases, one tooth may even be placed completely behind another.</p>
					<br>
					<p>Some possible problems of crooked teeth are:
						<br>
						<br>
						<span><b>° Difficulty performing proper dental hygiene: </b></span> Due to this, the risk of presenting
						caries and periodontal disease increases.
						<br>
						<br>
						<span><b>° Gingival recessions: </b></span> As teeth migrate out of the bone, gingival recessions can occur
						over time.
						<br>
						<br>
						<span><b>° Dental attrition: </b></span> Because the teeth do not mesh properly, they are subject to
						excessive wear.
						<br>
						<br>
					</p>
					<h2 id="subtitulo2">CAUSES</h2>
					<p>There are several reasons why teeth can grow crooked:
						<br>
						<br>
					<p>
						-Genetics
						<br>
						-Bad habits
						<br>
						-Illness or injury
						<br>
						-Use of braces, ill-fitting dentures, and other appliances that may rub against the mouth
					</p>
					<br>
					<br>
					<img src="https://i.ytimg.com/vi/l1bmUrQ7JQI/maxresdefault.jpg">
					<br><br>
					<hr>
					<h2 id="subtitulo3">TREATMENTS</h2><br>
					<p>The ideal treatment for crooked teeth is orthodontic treatment, which can be started at any age. Even so, there is a better time to start treatment. If it is started at an early age, a better result can be guaranteed.
						<br><br>
						In adult patients there is also the possibility of correcting the alignment of the teeth by placing veneers or crowns.
					</p>
